    private void tblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDatosMouseClicked
        //No existe un evento doble click por ello se simula
        if (evt.getClickCount() == 2) { //Se dió doble click
            //Para obtener la fila que se le dió doble click
            //Para obtener el valor los hacemos con getValue()
            int fila = tblDatos.rowAtPoint(evt.getPoint());
            //Obtener el id de la primera columna
            txtCodigo.setText(tblDatos.getValueAt(fila, 0).toString());
            //Obtener el nombre de la segunda columna
            txtNombre.setText(tblDatos.getValueAt(fila, 1).toString());
            //Obtener el nombre de la segunda columna
            cboMarca.setSelectedItem(tblDatos.getValueAt(fila, 2).toString());
            //Obtener el nombre de la segunda columna
            txtPrecio.setText(tblDatos.getValueAt(fila, 3).toString());
        }
    }//GEN-LAST:event_tblDatosMouseClicked


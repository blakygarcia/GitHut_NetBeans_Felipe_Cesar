    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        //Crear la instancia del modal
        //El primer paramétro es el propietario, como es internal no puede ser propietario de nadie
        //El segundo paramétro es sí es un modal
        dlgBuscar buscar = new dlgBuscar(null, true);
        //Sobreescribir el método cerrar
        //para que al cerrarse los datos se carguen en el formulario que lo llamó
        //Es similar a Javascript addEventListener, que está atento a un evento
        buscar.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent e) {
                //Recupera el número de la fila
                //cuando el usuario da doble click y se cierra el modal
                int fila = buscar.ObtenerId();

                //El dato de la fila seleccionada primera columna
                txtCodigo.setText(tblDatos.getValueAt(fila, 0).toString());
                //El dato de la fila seleccionada segunda columna
                txtNombre.setText(tblDatos.getValueAt(fila, 1).toString());
                //El dato de la fila seleccionada tercera columna
                cboMarca.setSelectedItem(tblDatos.getValueAt(fila, 2).toString());
                //El dato de la fila seleccionada cuarta columna
                txtPrecio.setText(tblDatos.getValueAt(fila, 3).toString());
                
            }
        });
        //Mostrar la instancia del formulario de búsquedas - mostrar formulario
        buscar.setVisible(true);
    }//GEN-LAST:event_btnBuscarActionPerformed


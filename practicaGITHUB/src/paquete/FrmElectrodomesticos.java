    public void CargarDatos(){
        //Crear un arreglo de objetos para guardar los datos
        Object[] fila = new Object[4];
        
        //Verificar que los campos tienen algo
        if (!txtCodigo.getText().equals("") && !txtNombre.getText().equals("") && !txtPrecio.getText().equals("") && !cboMarca.getSelectedItem().equals("Seleccione")) {
            fila[0] = txtCodigo.getText();
            fila[1] = txtNombre.getText();
            fila[2] = cboMarca.getSelectedItem();
            fila[3] = txtPrecio.getText();
            //modelo es global
            modelo.addRow(fila);
        }else{
            //Mensaje si no hay datos
            JOptionPane.showMessageDialog(this, "Faltan datos");
        }
    }
    
    public void Limpiar(){
        txtCodigo.setText(null);
        txtNombre.setText(null);
        cboMarca.setSelectedIndex(0);
        txtPrecio.setText(null);
    }
    
